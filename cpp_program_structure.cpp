#include <iostream>

int main()
{
    std::cout << "Hello World!" << std::endl;  // endl --> end line, a constant defined in std namespace
    std::cout << "Welcome to C++ Programming"; // no default new line added after printing
    std::cout << "Welcome to C++ Programming"; // no default new line added after printing
    std::cout << "Welcome to my github";
    return 0;
}

// std --> standard namespace
// cout --> console output
// endl --> end line, a constant defined in std namespace
// << --> insertion operator
// \n --> new line
// :: --> scope operator
